indice = 12  # valor até onde repetição vai 
soma = 0  # variável que vai acumular o total da soma
k = 0  # variavel de controle para o loop

# enquanto o valor de k for menor que indice, continue somando
while k < indice:
    k += 1  # incrementa k em 1
    soma += k  # adiciona o valor de k à soma

print(soma)  # imprime o resultado final da soma
